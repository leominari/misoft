    private void btnEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntrarActionPerformed
        user.setUsuario(tfUsuario.getText());
        user.setSenha(pfSenha.getText());
        CLogin lg = new CLogin();
        if (verificaCampos()) {

            try {
                this.user.setId(lg.buscaUser(user));
            } catch (SQLException ex) {
                Logger.getLogger(VLogin.class.getName()).log(Level.SEVERE, null, ex);
            }

            if (lg.doLogin(user)) {
                VMenu vm = new VMenu(user);
                vm.setVisible(true);
                new Janela().btnBarra(vm);
                this.dispose();
            }

        } else {
            JOptionPane.showMessageDialog(null, "Preecha os campos obrigat칩rios.");

        }


    }//GEN-LAST:event_btnEntrarActionPerformed

